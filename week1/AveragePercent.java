// Create AveragePercent Class to compute the average percentage mark in pcm .
public class AveragePercent{
	public static void main(String[] args){
		
		// Create a int variable maths,physics,chemistry  
		//and assign value 1, value 2, value 3 respectively	
		double maths = 94,physics = 95,chemistry = 96;
	
		//create variable average to calculate the average percent 
		//mark in PCM.
		double average = (maths + physics + chemistry)/3;
	
		//Print the average detail
		System.out.println("the average percent mark in PCM :"+ average );
	
	}
}